// Undefined
let numero1;
console.log("numero1:", numero1);
console.log("typeof(numero1)", typeof(numero1));

// Null
let numero2 = null;
console.log("numero2:", numero2);
console.log("typeof(numero2)", typeof(numero2));

// Comparando igualdad entre undefined y null
console.log("numero1 == numero2:", numero1 == numero2);

// Comparando igualdad estricta entre undefined y null
console.log("numero1 === numero2:", numero1 === numero2);

// Comparando desigualdad entre undefined y null
console.log("numero1 != numero2:", numero1 != numero2);

// Comparando desigualdad estricta entre undefined y null
console.log("numero1 !== numero2:", numero1 !== numero2);