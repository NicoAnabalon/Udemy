// Definiendo una función
function sumar(a, b) {
    console.log(`La suma de ${a} + ${b} es igual a ${a + b}`);
}

// Llamando una función con diversos parametros
sumar(2, 3);
sumar (200, 184);

// Definiendo una función para saludar
function saludar(nombre, apellido){
    console.log(`¡Bienvenido ${nombre} ${apellido}!`)
}

// Llamando a la función con ambos parametros
saludar("Nicolás", "Anabalón");
// Llamando a la función con sólo 1 parametro
saludar("Nicolás");