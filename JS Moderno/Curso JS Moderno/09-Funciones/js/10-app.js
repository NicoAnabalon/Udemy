// Declaración de función normal
const aprendiendo = function() {
    console.log("Aprendiendo JavaScript");
}
// Llamando a la función normal
aprendiendo();

// Declaración de función de flecha (Arrow Function)
const aprendiendo2 = () => {
    console.log("Aprendiendo JavaScript");
}
// Llamando a la función de flecha
aprendiendo2()

// Arrow Function con 1 sola instrucción
const aprendiendo3 = () => console.log("Aprendiendo JavaScript");
// Llamando a la arrow function
aprendiendo3()