const producto = 'Monitor 20 pulgadas';
console.log(producto);

// Repetir una cadena de texto
// repeat(x) donde x es la cantidad de veces se repetira
console.log(producto.repeat(3));
// x se redondea
console.log(producto.repeat(3.2));

// Dividir un string
// split(x) donde x es el string mediante el cual se separara
console.log(producto.split(" "));
