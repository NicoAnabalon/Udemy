const booleanTrue = true;
const booleanFalse = false;

// Comparando estrictamente booleanTrue y booleanFalse
console.log("Comparacion estricta entre booleanTrue y booleanFalse:", booleanTrue === booleanFalse);

// Comparando estrictamente booleanTrue y true
console.log("Comparacion estricta entre booleanTrue y true:", booleanTrue === true);

// Comparando estrictamente booleanFalse y false
console.log("Comparacion estricta entre booleanFalse y false:", booleanFalse === false);

// Comparando estrictamente booleanTrue y "true"
console.log("Comparacion estricta entre booleanTrue y \"true\"", booleanTrue === "true");

// Comparando estrictamente booleanFalse y "false"
console.log("Comparacion estricta entre booleanFalse y \"false\"", booleanFalse === "false");