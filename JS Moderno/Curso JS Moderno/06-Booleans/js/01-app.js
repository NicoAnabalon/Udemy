// Booleanos tienen solo 2 posibles valores  true o false
// Boolean true
const booleanTrue = true;
console.log("booleanTrue:", booleanTrue);
console.log("Tipo booleanTrue:", typeof booleanTrue);
// Boolean false
const booleanFalse = false;
console.log("booleanFalse:", booleanFalse);
console.log("Tipo booleanFalse:", typeof booleanFalse);

// Se puede crear un boolean como objeto
// Objeto booleanTrue
const booleanTrueObject = new Boolean(true);
console.log("booleanTrueObject:", booleanTrueObject);
console.log("Tipo booleanTrueObject:", typeof booleanTrueObject);
// Objeto booleanFalse
const booleanFalseObject = new Boolean(false);
console.log("booleanFalseObject:", booleanFalseObject);
console.log("Tipo booleanFalseObject:", typeof booleanFalseObject);

// Los booleanos se definen sin ""
// String boolean true
const stringBooleanTrue = "true";
console.log("stringBooleanTrue:", stringBooleanTrue);
console.log("Tipo stringBooleanTrue:", typeof stringBooleanTrue);
// String boolean fakse
const stringBooleanFalse = "false";
console.log("stringBooleanFalse:", stringBooleanFalse);
console.log("Tipo stringBooleanFalse:", typeof stringBooleanFalse);