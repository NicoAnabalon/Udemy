// Instanciando Date
const diaHoy = new Date();
console.log("Dia hoy:", diaHoy);

// Obteniendo el año
console.log("Año:", diaHoy.getFullYear());

// Obteniendo el mes
console.log("Mes:", diaHoy.getMonth() + 1);

// Obteniendo el día
console.log("Día:", diaHoy.getDay());

// Obteniendo la hora
console.log("Hora:", diaHoy.getHours());

// Obteniendo los minutos
console.log("Minutos:", diaHoy.getMinutes());

// Obteniendo los segundos
console.log("Segundos:", diaHoy.getSeconds());