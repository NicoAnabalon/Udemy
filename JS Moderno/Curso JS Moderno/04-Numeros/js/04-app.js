// Orden de operaciones

// Multiplicaciones se ejecutan primero
resultado = 20 + 30 * 2;
console.log(resultado);

// Parentesis se ejecutan primero
resultado2 = (20 + 30) * 2;
console.log(resultado2);

// 20% descuento
resultado3 = (20 + 30 + 40 + 50 + 60) * .2;
console.log(resultado3);

// Agregando el 16%
resultado4 = (20 + 30) * 1.16;
console.log(resultado4);

